namespace SDLTooSharp.Managed.Video.Window;

/// <summary>
/// Enumeration of window modes
/// </summary>
public enum WindowMode
{
    /// <summary>
    /// The window is in native fullscreen.
    /// </summary>
    Fullscreen = 0,

    /// <summary>
    /// The window is in desktop fullscreen (the display's resolution is unchanged)
    /// </summary>
    DesktopFullscreen = 1,

    /// <summary>
    /// The window is in windowed mode
    /// </summary>
    Windowed = 2
}
